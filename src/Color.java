public class Color {
    private byte red;
    private byte green;
    private byte blue;

    public Color(byte red, byte green, byte blue){
        this.blue = blue;
        this.green = green;
        this.blue = blue;
    }

    public byte getBlue() {
        return blue;
    }

    public byte getGreen() {
        return green;
    }

    public byte getRed() {
        return red;
    }

    public void setBlue(byte blue) {
        this.blue = blue;
    }

    public void setGreen(byte green) {
        this.green = green;
    }

    public void setRed(byte red) {
        this.red = red;
    }
}
